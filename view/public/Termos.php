    <script>
        // Destacar a seção ativa no índice ao rolar a página
        document.addEventListener('DOMContentLoaded', function() {
            const sections = document.querySelectorAll('.term-section');
            const navLinks = document.querySelectorAll('.table-of-contents a');
            
            window.addEventListener('scroll', function() {
                let current = '';
                
                sections.forEach(section => {
                    const sectionTop = section.offsetTop;
                    const sectionHeight = section.clientHeight;
                    
                    if (pageYOffset >= (sectionTop - 150)) {
                        current = section.getAttribute('id');
                    }
                });
                
                navLinks.forEach(link => {
                    link.classList.remove('active', 'bg-light', 'fw-bold');
                    if (link.getAttribute('href').substring(1) === current) {
                        link.classList.add('active', 'bg-light', 'fw-bold');
                    }
                });
            });
            
            // Rolagem suave para as seções ao clicar nos links do índice
            navLinks.forEach(link => {
                link.addEventListener('click', function(e) {
                    e.preventDefault();
                    
                    const targetId = this.getAttribute('href').substring(1);
                    const targetSection = document.getElementById(targetId);
                    
                    window.scrollTo({
                        top: targetSection.offsetTop - 100,
                        behavior: 'smooth'
                    });
                });
            });
        });
    </script>
